
#ifndef STD_TYPES_H

#define STD_TYPES_H


typedef unsigned char    u8  ;
typedef signed char      s8  ;
typedef unsigned short   u16 ;
typedef signed short     s16 ;
typedef unsigned int     u32 ;
typedef signed int       s32 ;
typedef float            f8  ;
typedef double           f32 ;


#define   STD_TYPES_OK      1
#define   STD_TYPES_NOK     0

#define   NULL          ((void *)0)


#endif

